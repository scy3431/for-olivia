  /* journal: password gate, entries, composer, and the copy-to-file snippet */
  const JOURNAL_SESSION_KEY = "keepsake-journal-unlocked";
  let previewEntries = []; // entries added this session, before being pasted into script.js

  function isJournalUnlocked() {
    try {
      return sessionStorage.getItem(JOURNAL_SESSION_KEY) === "1";
    } catch (e) {
      return false;
    }
  }

  function setJournalUnlocked() {
    try {
      sessionStorage.setItem(JOURNAL_SESSION_KEY, "1");
    } catch (e) { /* noop */ }
  }

  function refreshJournalLockState() {
    const lock = document.getElementById("journal-lock");
    const content = document.getElementById("journal-content");
    if (!lock || !content) return;
    const unlocked = isJournalUnlocked();
    lock.hidden = unlocked;
    content.hidden = !unlocked;
    if (unlocked) buildJournalEntries();
  }

  function initJournalLock() {
    const form = document.getElementById("journal-lock-form");
    const input = document.getElementById("journal-password-input");
    const error = document.getElementById("journal-lock-error");
    if (!form) return;

    form.addEventListener("submit", (e) => {
      e.preventDefault();
      if (input.value === CONFIG.journalPassword) {
        error.hidden = true;
        input.value = "";
        setJournalUnlocked();
        refreshJournalLockState();
      } else {
        error.hidden = false;
      }
    });
  }

  function formatEntryDate(rawDate) {
    return rawDate && rawDate.trim() ? rawDate.trim() : "Undated";
  }

  function buildJournalEntries() {
    const container = document.getElementById("journal-entries");
    if (!container) return;
    container.innerHTML = "";

    const combined = journalEntries.concat(previewEntries);

    if (combined.length === 0) {
      const empty = document.createElement("p");
      empty.className = "journal-empty";
      empty.textContent = "No entries yet \u2014 tap the + to write the first one.";
      container.appendChild(empty);
      return;
    }

    combined.forEach((entry, i) => {
      const isPreview = i >= journalEntries.length;

      const card = document.createElement("div");
      card.className = "journal-entry";

      const dateRow = document.createElement("div");
      dateRow.className = "journal-entry__date";

      const dateText = document.createElement("span");
      dateText.textContent = formatEntryDate(entry.date);
      dateRow.appendChild(dateText);

      if (isPreview) {
        const tag = document.createElement("span");
        tag.className = "journal-entry__preview-tag";
        tag.textContent = "preview";
        dateRow.appendChild(tag);
      } else {
        const hint = document.createElement("span");
        hint.className = "journal-entry__hint";
        hint.textContent = "tap to read";
        dateRow.appendChild(hint);
      }

      const textEl = document.createElement("p");
      textEl.className = "journal-entry__text";
      textEl.textContent = entry.text || "";

      card.appendChild(dateRow);
      card.appendChild(textEl);
      card.addEventListener("click", () => card.classList.toggle("is-open"));

      container.appendChild(card);
    });
  }

  function escapeForJsString(str) {
    return String(str).replace(/\\/g, "\\\\").replace(/"/g, '\\"').replace(/\n/g, "\\n");
  }

  function initJournalComposer() {
    const addButton = document.getElementById("journal-add-button");
    const composer = document.getElementById("journal-composer");
    const dateInput = document.getElementById("journal-composer-date");
    const textInput = document.getElementById("journal-composer-text");
    const cancelBtn = document.getElementById("journal-composer-cancel");
    const saveBtn = document.getElementById("journal-composer-save");

    const snippetOverlay = document.getElementById("journal-snippet");
    const snippetTextarea = document.getElementById("journal-snippet-text");
    const snippetCopy = document.getElementById("journal-snippet-copy");
    const snippetDone = document.getElementById("journal-snippet-done");

    if (!addButton) return;

    function openComposer() {
      const today = new Date();
      dateInput.value = today.toLocaleDateString(undefined, {
        year: "numeric",
        month: "long",
        day: "numeric",
      });
      textInput.value = "";
      composer.hidden = false;
      window.setTimeout(() => textInput.focus(), 50);
    }

    function closeComposer() {
      composer.hidden = true;
    }

    addButton.addEventListener("click", openComposer);
    cancelBtn.addEventListener("click", closeComposer);
    composer.addEventListener("click", (e) => {
      if (e.target === composer) closeComposer();
    });

    saveBtn.addEventListener("click", () => {
      const date = dateInput.value.trim();
      const text = textInput.value.trim();
      if (!text) {
        textInput.focus();
        return;
      }

      previewEntries.push({ date, text });
      closeComposer();
      buildJournalEntries();

      const snippet =
        `  { date: "${escapeForJsString(date)}", text: "${escapeForJsString(text)}" },`;
      snippetTextarea.value = snippet;
      snippetOverlay.hidden = false;
    });

    snippetDone.addEventListener("click", () => {
      snippetOverlay.hidden = true;
    });
    snippetOverlay.addEventListener("click", (e) => {
      if (e.target === snippetOverlay) snippetOverlay.hidden = true;
    });

    snippetCopy.addEventListener("click", () => {
      snippetTextarea.select();
      try {
        navigator.clipboard.writeText(snippetTextarea.value);
      } catch (e) {
        document.execCommand("copy");
      }
      snippetCopy.textContent = "copied!";
      window.setTimeout(() => { snippetCopy.textContent = "copy snippet"; }, 1600);
    });
  }

